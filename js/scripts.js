function Person(firstName, lastName, age) {
  this.firstName = firstName;
  this.lastName = lastName;
  this.age = age;
  this.budget = "";
  this.animalType = [];
  this.personalityHuman = "";
}

function Animal(animalName){
  this.animalName = animalName;
  this.ageCategory = "";
  this.species = "";
  this.breed = "";
  this.temperament = [];
  this.profilePic = "";
}
